    private void saveForStreamBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveForStreamBtnActionPerformed
        String stream = streamIDs.get(streamsCmb.getSelectedIndex());
        double sf = Double.parseDouble(sfTxt.getText());
        double lr = Double.parseDouble(lrTxt.getText());
        int iter = Integer.parseInt(iterTxt.getText());
        int nr = Integer.parseInt(nrTxt.getText());
        int ht = Integer.parseInt(htTxt.getText());
        int dim = Integer.parseInt(dimTxt.getText());
        AggregationType aggr = AggregationType.FUZZY;
        if (aggrCmb.getSelectedIndex() == 1) {
            aggr = AggregationType.AVERAGE;
        } else if (aggrCmb.getSelectedIndex() ==2){
            aggr = AggregationType.NONE;
        }
        AlgoParamModel pModel = new AlgoParamModel(stream, sf, nr, lr, iter, ht, aggr,dim);
        streamParamMap.put(stream, pModel);
    }//GEN-LAST:event_saveForStreamBtnActionPerformed

