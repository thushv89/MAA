    private void finishBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finishBtnActionPerformed

        AlgoXMLWriter aXMLWriter = new AlgoXMLWriter();

        if (!sameValChk.isSelected() &&  streamParamMap.keySet().containsAll(streamIDs)) {
            String loc = ImportantFileNames.CONFIG_DIRNAME + File.separator + ImportantFileNames.ALGO_CONFIG_FILENAME;
            aXMLWriter.writeMultipleXML(new ArrayList<String>(streamParamMap.keySet()), new ArrayList<AlgoParamModel>(streamParamMap.values()), loc);

            cListener.allConfigCompleted();
        }
        if(sameValChk.isSelected()){
            //use same value for all the elements
        }

    }//GEN-LAST:event_finishBtnActionPerformed
